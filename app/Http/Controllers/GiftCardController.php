    public function generateQr(){

        $values = [0.01,0.005,0.001,0.0005,0.0001];
        for($t=0;$t<count($values);$t++){
            $giftCards = GiftCard::where('btc',$values[$t])->get();
            foreach($giftCards as $index => $giftCard){

                $qr = QrCode::format('png')->backgroundColor(210,168,103)->size(90)->generate($giftCard->pu_key);
                $output_file = "/img/qr-codes-$values[$t]/" .$giftCard->pu_key. '.png';
                Storage::disk('local')->put($output_file, $qr);

                $qr = QrCode::format('png')->backgroundColor(210,168,103)->size(90)->generate($giftCard->pr_key);
                $output_file = "/img/qr-codes-$values[$t]/" . $giftCard->pr_key . '.png';
                Storage::disk('local')->put($output_file, $qr);
            }
        }

        return 'done';
    }

